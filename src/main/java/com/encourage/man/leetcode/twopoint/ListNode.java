package com.encourage.man.leetcode.twopoint;

/**
 * @author machen
 */
public class ListNode {
    public Integer val;
    public ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
